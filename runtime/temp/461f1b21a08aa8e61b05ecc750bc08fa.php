            var ab_name = $("input[name='ab_name']").val();
            var ab_phone = $("input[name='ab_phone']").val();
            var ab_email = $("input[name='ab_email']").val();
            var ab_company = $("input[name='ab_company']").val();
            var ab_content = $("textarea[name='ab_content']").val();
            $.ajax({
                type: "POST",
                data: { ab_name: ab_name, ab_phone: ab_phone, ab_email: ab_email, ab_company: ab_company, ab_content: ab_content },
                url: "<?php echo url('about/index'); ?>",
                dataType: "json",
                success: function (data) {
                    if (data.error == "2") {
                        layer.msg("<span>" + data.msg + "</span>");
                    }else if(data.error == "0"){
                        layer.msg("<span>" + data.msg + "</span>");
                    }else if(data.error == "0" && data.error == "1"){
                        layer.msg("<span>" + data.msg + "</span>");
                    }
                }
            });
            return false;
        })
    </script>
